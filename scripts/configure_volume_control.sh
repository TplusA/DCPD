#! /bin/sh

if test $# -ge 2
then
    DEST="$1"
    shift
else
    DEST='r1000e'
fi

case $1
in
    percent)
        BYTES='64 0x01  0x00  0x00 0x00  0x06 0x40  0x00 0x10  0x25 0xa0  0x00 0x78  0x01 0x90  0x00'
        ;;
    percent_muted)
        BYTES='64 0x01  0x00  0x00 0x00  0x06 0x40  0x00 0x10  0x24 0xa4  0x00 0xa0  0x02 0x10  0x01'
        ;;
    percent_uninitialized)
        BYTES='64 0x01  0x00  0x00 0x00  0x06 0x40  0x00 0x10  0x25 0xa0  0x00 0x78  0x20 0x00  0xff'
        ;;
    db)
        BYTES='64 0x01  0x01  0x23 0xc8  0x00 0x60  0x00 0x04  0x23 0xc8  0x00 0x60  0x21 0xe0  0x00'
        ;;
    db_muted)
        BYTES='64 0x01  0x01  0x22 0xc0  0x00 0x78  0x00 0x08  0x22 0xc0  0x00 0x78  0x22 0x70  0x01'
        ;;
    db_uninitialized)
        BYTES='64 0x01  0x01  0x25 0x00  0x01 0x30  0x00 0x18  0x25 0x00  0x01 0x30  0x20 0x00  0xff'
        ;;
    pm)
        BYTES='64 0x01  0x02  0x20 0x00  0x20 0x00  0x00 0x10  0x20 0x00  0x20 0x00  0x20 0x00  0x00'
        ;;
    pm_muted)
        BYTES='64 0x01  0x02  0x20 0x00  0x20 0x00  0x00 0x10  0x20 0x00  0x20 0x00  0x20 0x00  0x01'
        ;;
    pm_uninitialized)
        BYTES='64 0x01  0x02  0x20 0x00  0x20 0x00  0x00 0x10  0x20 0x00  0x20 0x00  0x20 0x00  0xff'
        ;;
    clear)
        BYTES='64 0x02'
        ;;

    *)
        echo 'Invalid configuration preset'
        exit 1
        ;;
esac

exec ./send_dcp_command.sh "$DEST" -- w $BYTES
