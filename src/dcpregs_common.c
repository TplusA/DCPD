#if HAVE_CONFIG_H
#include <config.h>
#endif /* HAVE_CONFIG_H */

#include "dcpregs_common.h"

size_t dcpregs_trim_trailing_zero_padding(const uint8_t *data, size_t length)
{
    while(length > 0 && data[length - 1] == '\0')
        --length;

    return length;
}
